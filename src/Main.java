public class Main {
    public static void main(String[] args) {
        // Задача №1
        for (int a = 1; a <= 10; a ++) {
            System.out.println(a);
        }
        // задача №2
        for (int b = 10; b > 0; b --) {
            System.out.println(b);
        }
        // задача №3
        for (int c = 0; c < 17; c = c + 2) {
            System.out.println(c);
        }
        // задача №4
        for (int d = 10; d >= (-10); d --) {
            System.out.println(d);
        }
    }
}